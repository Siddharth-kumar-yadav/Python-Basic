def rev_str(input_string):
    return input_string[::-1]

# Example usage:
input_str1 = "Siddharth"
input_str2 = "Kumar"
input_str3 = "Yadav"

print(f"The reverse of '{input_str1}' is: '{rev_str(input_str1)}'")
print(f"The reverse of '{input_str2}' is: '{rev_str(input_str2)}'")
print(f"The reverse of '{input_str3}' is: '{rev_str(input_str3)}'")
